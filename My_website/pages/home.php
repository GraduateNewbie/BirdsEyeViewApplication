	<h2>common garden birds</h2>
	</div>
<p class="col-lg-5 col-lg-offset-1 col-md-6 col-sm-12 col-xs-12">    

Large birds such as magpies, carrion crows, collared doves and jackdaws are also often seen, particularly if you have an open bird table. These distinctive birds can be hard to miss when they appear and are a familiar sight to most of us.

More numerous but harder to spot are the several species of small, brown birds that regularly show up at the bird feeder.

The wren is one of the UKâ€™s smallest birds and with its small, rounded body and upright tail it is often seen in gardens. It is particularly noticeable in spring when male wrens build multiple nests for the female to choose from. A similar but more colourful and even smaller bird is the goldcrest, although this little bird is less often seen in gardens. Read more about the goldcrest.

Another common garden bird is the dunnock, a small brown and grey bird with mottled feathers which provide excellent </p>
	
  <p class="col-lg-5 col-md-6 col-sm-12 col-xs-12">camouflage, making it hard to spot, although competing males can put on quite a display. Read more about the dunnock. Female blackbirds can be harder to identify due to their drab colouring, compared to the males, but have beautifully delicate markings if you look closely.

Song thrushes were once a very popular sight in gardens but their numbers are in serious decline, making them much less likely to visit your garden.

As well as blue tits and great tits, coal tits and long-tailed tits are regular visitors and can usually be seen in small flocks. Blue tits, great tits and coal tits might be persuaded to use a well-chosen nest box, but long-tailed tits prefer to build their nests out of lichen and spider webs in hedgerows, the shape of which has led so some amusing nicknames such as Oven builder, Bum barrel and Poke pudding.</p>
