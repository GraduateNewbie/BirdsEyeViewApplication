<b>Bird feeding:</b>

A bird feeder or tray feeder are devices placed outdoors is to supply bird food to birds. The success of a bird feeder in attracting birds depends upon its placement and the kinds of foods offered, as different species have different preferences.

Bird feeders often are used for birdwatching and many people keep webcams trained on feeders where birds often congregate.

Seed feeders are the most common type. These can vary in design from tubes to hoppers and trays. Sunflower seed or mixed seed are popular for use in these feeders and will attract many songbirds such as cardinals, finches, and chickadees. The outer shell of Black Oil Sunflower seeds are thinner and easier to crack than other types of Sunflower seeds.  Black Oil Sunflower seeds also contain a large amount of fat; therefore they are especially good to use in the winter. Most bird feeders are designed to dispense sunflower-sized foods, but there are specialty “finch feeders” with smaller openings to dispense the tiny Guizotia abyssinica (Niger seed), which is a favorite of smaller finches.

